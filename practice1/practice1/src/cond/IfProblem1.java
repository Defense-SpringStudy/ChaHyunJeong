package cond;

public class IfProblem1 {

    public static void main(String[] args) {

        int rate = 7;

        if(rate <= 9){
            System.out.println("어바웃타임 추춴");
        }
        if (rate <= 8) {
            System.out.println("토이스토리 추천");
        }
        if (rate <= 7){
            System.out.println("고질라 추천");
        }
    }
}
